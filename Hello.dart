void main() {
  //Strings
  String firstName = "John";
  print("firstname is: $firstName");

  //Integers
  int myNum = 49;
  print("Integer is: $myNum");

  //Doubles
  double decimalNum = 10.3;
  print("Decimal number is: $decimalNum");

  //Booleans
  bool myBool = true;
  print("Boolean value is: $myBool");

  //Dynamic
  dynamic fullName = "elon musk";
  print("Fullname is: $fullName");
}
